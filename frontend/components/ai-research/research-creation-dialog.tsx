'use client';

import { useState, useCallback } from 'react';
import { Search, RefreshCw, Gauge, Target } from 'lucide-react';
import { cn } from '@/lib/utils/common';
import { Modal } from '@/components/ui/dialogs/Modal';

export interface ResearchCreationOptions {
  mode: 'single' | 'iterative';
  depth: 'quick' | 'standard' | 'thorough';
  language: string;
  includeAcademic: boolean;
  maxIterations?: number;
  qualityThreshold?: number;
  autoGenerateDemo?: boolean;
}

interface ResearchCreationDialogProps {
  open: boolean;
  onClose: () => void;
  query: string;
  onQueryChange: (query: string) => void;
  onStart: (query: string, options: ResearchCreationOptions) => void;
  language: string;
}

export const DEFAULT_OPTIONS: ResearchCreationOptions = {
  mode: 'single',
  depth: 'standard',
  language: 'zh-CN',
  includeAcademic: true,
  maxIterations: 4,
  qualityThreshold: 75,
  autoGenerateDemo: true,
};

export function ResearchCreationDialog({
  open,
  onClose,
  query,
  onQueryChange,
  onStart,
  language,
}: ResearchCreationDialogProps) {
  const [options, setOptions] = useState<ResearchCreationOptions>({
    ...DEFAULT_OPTIONS,
    language,
  });

  const handleStart = useCallback(() => {
    if (!query.trim()) return;
    onStart(query.trim(), options);
    onClose();
  }, [query, options, onStart, onClose]);

  const depthOptions = [
    { value: 'quick' as const, label: '快速', desc: '1-2 轮搜索' },
    { value: 'standard' as const, label: '标准', desc: '3-5 轮搜索' },
    { value: 'thorough' as const, label: '深入', desc: '5-8 轮搜索' },
  ];

  return (
    <Modal
      open={open}
      onClose={onClose}
      title="开始深度研究"
      size="md"
      footer={
        <div className="flex items-center justify-end gap-3">
          <button
            onClick={onClose}
            className="rounded-lg px-4 py-2 text-sm font-medium text-gray-600 transition-colors hover:bg-gray-100"
          >
            取消
          </button>
          <button
            onClick={handleStart}
            disabled={!query.trim()}
            className={cn(
              'rounded-lg px-5 py-2 text-sm font-medium transition-all',
              query.trim()
                ? 'bg-purple-600 text-white hover:bg-purple-700'
                : 'cursor-not-allowed bg-gray-200 text-gray-400'
            )}
          >
            开始研究
          </button>
        </div>
      }
    >
      <div className="space-y-5">
        {/* Query Input */}
        <div>
          <label className="mb-1.5 block text-sm font-medium text-gray-700">
            研究问题
          </label>
          <textarea
            value={query}
            onChange={(e) => onQueryChange(e.target.value)}
            placeholder="输入你想深入研究的问题..."
            rows={3}
            className="w-full rounded-lg border border-gray-300 px-3 py-2 text-sm text-gray-900 placeholder-gray-400 transition-colors focus:border-purple-400 focus:outline-none focus:ring-2 focus:ring-purple-100"
          />
        </div>

        {/* Research Mode */}
        <div>
          <label className="mb-2 block text-sm font-medium text-gray-700">
            研究模式
          </label>
          <div className="grid grid-cols-2 gap-3">
            <button
              onClick={() => setOptions((o) => ({ ...o, mode: 'single' }))}
              className={cn(
                'flex items-start gap-3 rounded-lg border-2 p-3 text-left transition-all',
                options.mode === 'single'
                  ? 'border-purple-500 bg-purple-50'
                  : 'border-gray-200 hover:border-gray-300'
              )}
            >
              <Search className="mt-0.5 h-5 w-5 flex-shrink-0 text-purple-600" />
              <div>
                <div className="text-sm font-medium text-gray-900">
                  单次研究
                </div>
                <div className="mt-0.5 text-xs text-gray-500">
                  单轮搜索分析，快速获取结果
                </div>
              </div>
            </button>
            <button
              onClick={() => setOptions((o) => ({ ...o, mode: 'iterative' }))}
              className={cn(
                'flex items-start gap-3 rounded-lg border-2 p-3 text-left transition-all',
                options.mode === 'iterative'
                  ? 'border-blue-500 bg-blue-50'
                  : 'border-gray-200 hover:border-gray-300'
              )}
            >
              <RefreshCw className="mt-0.5 h-5 w-5 flex-shrink-0 text-blue-600" />
              <div>
                <div className="text-sm font-medium text-gray-900">
                  迭代研究
                </div>
                <div className="mt-0.5 text-xs text-gray-500">
                  多轮优化，自动提升质量
                </div>
              </div>
            </button>
          </div>
        </div>

        {/* Research Depth */}
        <div>
          <label className="mb-2 block text-sm font-medium text-gray-700">
            研究深度
          </label>
          <div className="flex gap-2">
            {depthOptions.map((d) => (
              <button
                key={d.value}
                onClick={() => setOptions((o) => ({ ...o, depth: d.value }))}
                className={cn(
                  'flex-1 rounded-lg border px-3 py-2 text-center transition-all',
                  options.depth === d.value
                    ? 'border-purple-500 bg-purple-50 text-purple-700'
                    : 'border-gray-200 text-gray-600 hover:border-gray-300'
                )}
              >
                <div className="text-sm font-medium">{d.label}</div>
                <div className="mt-0.5 text-xs text-gray-400">{d.desc}</div>
              </button>
            ))}
          </div>
        </div>

        {/* Iterative Options (only when iterative mode) */}
        {options.mode === 'iterative' && (
          <div className="rounded-lg border border-blue-200 bg-blue-50/50 p-4">
            <div className="mb-3 flex items-center gap-2 text-sm font-medium text-blue-800">
              <Gauge className="h-4 w-4" />
              迭代选项
            </div>
            <div className="space-y-4">
              {/* Max Iterations */}
              <div>
                <div className="mb-1 flex items-center justify-between">
                  <label className="text-xs font-medium text-gray-600">
                    最大迭代轮数
                  </label>
                  <span className="text-xs font-bold text-blue-600">
                    {options.maxIterations}
                  </span>
                </div>
                <input
                  type="range"
                  min={2}
                  max={8}
                  value={options.maxIterations}
                  onChange={(e) =>
                    setOptions((o) => ({
                      ...o,
                      maxIterations: Number(e.target.value),
                    }))
                  }
                  className="w-full accent-blue-600"
                />
                <div className="flex justify-between text-[10px] text-gray-400">
                  <span>2</span>
                  <span>8</span>
                </div>
              </div>

              {/* Quality Threshold */}
              <div>
                <div className="mb-1 flex items-center justify-between">
                  <label className="text-xs font-medium text-gray-600">
                    质量目标
                  </label>
                  <span className="text-xs font-bold text-blue-600">
                    {options.qualityThreshold}%
                  </span>
                </div>
                <input
                  type="range"
                  min={50}
                  max={95}
                  step={5}
                  value={options.qualityThreshold}
                  onChange={(e) =>
                    setOptions((o) => ({
                      ...o,
                      qualityThreshold: Number(e.target.value),
                    }))
                  }
                  className="w-full accent-blue-600"
                />
                <div className="flex justify-between text-[10px] text-gray-400">
                  <span>50%</span>
                  <span>95%</span>
                </div>
              </div>

              {/* Auto Generate Demo */}
              <label className="flex cursor-pointer items-center gap-2">
                <input
                  type="checkbox"
                  checked={options.autoGenerateDemo}
                  onChange={(e) =>
                    setOptions((o) => ({
                      ...o,
                      autoGenerateDemo: e.target.checked,
                    }))
                  }
                  className="h-4 w-4 rounded border-gray-300 text-blue-600 focus:ring-blue-500"
                />
                <span className="text-xs font-medium text-gray-600">
                  自动生成 Demo
                </span>
              </label>
            </div>
          </div>
        )}

        {/* Advanced Options */}
        <div className="border-t border-gray-100 pt-4">
          <div className="mb-2 flex items-center gap-2 text-xs font-medium text-gray-500">
            <Target className="h-3.5 w-3.5" />
            高级选项
          </div>
          <label className="flex cursor-pointer items-center gap-2">
            <input
              type="checkbox"
              checked={options.includeAcademic}
              onChange={(e) =>
                setOptions((o) => ({ ...o, includeAcademic: e.target.checked }))
              }
              className="h-4 w-4 rounded border-gray-300 text-purple-600 focus:ring-purple-500"
            />
            <span className="text-xs font-medium text-gray-600">
              包含学术来源
            </span>
          </label>
        </div>
      </div>
    </Modal>
  );
}
