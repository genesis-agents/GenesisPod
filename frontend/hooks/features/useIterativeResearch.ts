import { useState, useCallback, useRef, useEffect } from 'react';
import { config } from '@/lib/utils/config';
import { getAuthHeader } from '@/lib/utils/auth';
import { logger } from '@/lib/utils/logger';
import type { DeepResearchReport } from './useDeepResearch';
import type {
  DiscussionMessage,
  DiscussionPhase,
  DiscussionRole,
  DiscussionMessageType,
  DiscussionResearchState,
} from './useDiscussionResearch';

// ==================== Iteration Types ====================

export interface IterationRound {
  round: number;
  score: number;
  previousScore: number;
  gaps: { dataGaps: string[]; ideaGaps: string[] };
  research?: {
    queries: string[];
    newSources: number;
    informationGain: number;
  };
  ideas?: {
    newInsights: Array<{ title: string }>;
    newCreativeIdeas: Array<{ title: string }>;
    totalInsights: number;
    totalCreativeIdeas: number;
  };
  demo?: {
    status: 'generating' | 'completed';
  };
  record?: string; // Markdown iteration record
  timestamp: Date;
}

export interface IterativeResearchState {
  discussion: DiscussionResearchState;
  mode: 'single' | 'iterative';
  currentRound: number;
  iterations: IterationRound[];
  exitReason: string | null;
  finalScore: number | null;
  totalIterations: number | null;
  isIterating: boolean;
  awaitingFeedback: {
    round: number;
    score: number;
    gaps: { dataGaps: string[]; ideaGaps: string[] };
    timeoutMs: number;
  } | null;
}

export interface IterativeResearchOptions {
  maxRounds?: number;
  includeAcademic?: boolean;
  language?: string;
  depth?: 'quick' | 'standard' | 'thorough';
  mode?: 'single' | 'iterative';
  maxIterations?: number;
  qualityThreshold?: number;
  autoGenerateDemo?: boolean;
}

export interface UseIterativeResearchOptions {
  onComplete?: (data: {
    report: DeepResearchReport;
    sessionId: string;
  }) => void;
  onError?: (error: string) => void;
  onIterationUpdate?: (round: IterationRound) => void;
  onIterationExit?: (data: {
    reason: string;
    finalScore: number;
    totalIterations: number;
    sessionId?: string;
  }) => void;
  onStreamEndIncomplete?: () => void;
}

// ==================== Initial State ====================

const initialDiscussionState: DiscussionResearchState = {
  phase: 'idle',
  messages: [],
  typingAgent: null,
  directions: [],
  searchProgress: null,
  reportContent: {},
  report: null,
  sessionId: null,
  error: null,
};

const initialIterativeState: IterativeResearchState = {
  discussion: initialDiscussionState,
  mode: 'single',
  currentRound: 0,
  iterations: [],
  exitReason: null,
  finalScore: null,
  totalIterations: null,
  isIterating: false,
  awaitingFeedback: null,
};

// ==================== Hook ====================

export function useIterativeResearch(
  projectId: string,
  options?: UseIterativeResearchOptions
): {
  state: IterativeResearchState;
  startResearch: (
    query: string,
    options?: IterativeResearchOptions
  ) => Promise<void>;
  stop: () => void;
  reset: () => void;
  isActive: boolean;
  sendFeedback: (text: string) => Promise<void>;
} {
  const [state, setState] = useState<IterativeResearchState>(
    initialIterativeState
  );
  const abortControllerRef = useRef<AbortController | null>(null);

  const callbacksRef = useRef(options);
  callbacksRef.current = options;

  // Refs for accumulated data to avoid stale closures in SSE loop
  const messagesRef = useRef<DiscussionMessage[]>([]);
  const directionsRef = useRef<string[]>([]);
  const iterationsRef = useRef<IterationRound[]>([]);
  const modeRef = useRef<'single' | 'iterative'>('single');

  const cleanup = useCallback(() => {
    if (abortControllerRef.current) {
      abortControllerRef.current.abort();
      abortControllerRef.current = null;
    }
  }, []);

  const handleEvent = useCallback(
    (eventType: string, data: Record<string, unknown>) => {
      switch (eventType) {
        case 'discussion.message': {
          const msg = data as unknown as DiscussionMessage;
          messagesRef.current = [...messagesRef.current, msg];
          setState((prev) => ({
            ...prev,
            discussion: {
              ...prev.discussion,
              messages: messagesRef.current,
              typingAgent: null,
            },
          }));
          break;
        }

        case 'discussion.phase': {
          const phaseData = data as {
            phase: DiscussionPhase;
            summary: string;
            directions?: string[];
          };
          const systemMsg: DiscussionMessage = {
            id: `phase_${Date.now()}`,
            agentRole: 'director',
            agentName: 'System',
            agentIcon: 'info',
            content: phaseData.summary,
            phase: phaseData.phase,
            messageType: 'system',
            metadata: phaseData.directions
              ? { directions: phaseData.directions }
              : undefined,
            timestamp: new Date(),
          };
          messagesRef.current = [...messagesRef.current, systemMsg];
          if (phaseData.directions) {
            directionsRef.current = phaseData.directions;
          }
          setState((prev) => ({
            ...prev,
            discussion: {
              ...prev.discussion,
              phase: phaseData.phase,
              messages: messagesRef.current,
              directions: phaseData.directions || prev.discussion.directions,
              typingAgent: null,
            },
          }));
          break;
        }

        case 'discussion.typing': {
          const typingData = data as { agentRole: string; agentName: string };
          setState((prev) => ({
            ...prev,
            discussion: {
              ...prev.discussion,
              typingAgent: {
                role: typingData.agentRole,
                name: typingData.agentName,
              },
            },
          }));
          break;
        }

        case 'search_progress': {
          const progress = data as {
            round: number;
            totalRounds: number;
            query: string;
            resultsCount: number;
            message: string;
          };
          setState((prev) => ({
            ...prev,
            discussion: {
              ...prev.discussion,
              searchProgress: {
                currentRound: progress.round,
                totalRounds: progress.totalRounds,
                query: progress.query,
                resultsCount: progress.resultsCount,
                message: progress.message,
              },
            },
          }));
          break;
        }

        case 'content.delta': {
          const delta = data as { section: string; delta: string };
          setState((prev) => ({
            ...prev,
            discussion: {
              ...prev.discussion,
              reportContent: {
                ...prev.discussion.reportContent,
                [delta.section]:
                  (prev.discussion.reportContent[delta.section] || '') +
                  delta.delta,
              },
            },
          }));
          break;
        }

        case 'interaction.complete': {
          const completeData = data as {
            sessionId: string;
            report: DeepResearchReport;
            status: string;
          };

          if (modeRef.current === 'iterative') {
            // In iterative mode, intermediate completions do not finalize the session.
            // IMPORTANT: Do NOT update sessionId here — completeData.sessionId is the
            // intermediate session which the backend deletes after syncing the report
            // to the original session. Using a deleted sessionId would cause data loss
            // on page refresh or SSE reconnect.
            setState((prev) => ({
              ...prev,
              discussion: {
                ...prev.discussion,
                // Keep prev.discussion.sessionId (the original session) intact
                report: completeData.report,
                typingAgent: null,
              },
              isIterating: true,
            }));
          } else {
            setState((prev) => ({
              ...prev,
              discussion: {
                ...prev.discussion,
                phase: 'completed',
                sessionId: completeData.sessionId,
                report: completeData.report,
                typingAgent: null,
              },
              isIterating: false,
            }));
            callbacksRef.current?.onComplete?.({
              report: completeData.report,
              sessionId: completeData.sessionId,
            });
            if (abortControllerRef.current) {
              abortControllerRef.current.abort();
              abortControllerRef.current = null;
            }
          }
          break;
        }

        case 'iteration.session': {
          const sessionData = data as { sessionId: string };
          setState((prev) => ({
            ...prev,
            discussion: {
              ...prev.discussion,
              sessionId: sessionData.sessionId,
            },
          }));
          break;
        }

        case 'iteration.start': {
          const startData = data as {
            round: number;
            targetGaps: { dataGaps: string[]; ideaGaps: string[] };
          };
          const separatorMsg: DiscussionMessage = {
            id: `iter_start_${startData.round}_${Date.now()}`,
            agentRole: 'director' as DiscussionRole,
            agentName: '迭代系统',
            agentIcon: 'refresh-cw',
            content: `── 第 ${startData.round} 轮迭代开始 ──\n目标: ${[...startData.targetGaps.dataGaps.slice(0, 2), ...startData.targetGaps.ideaGaps.slice(0, 2)].join('、') || '深度优化'}`,
            phase: 'ideation' as DiscussionPhase,
            messageType: 'system' as DiscussionMessageType,
            timestamp: new Date(),
          };
          messagesRef.current = [...messagesRef.current, separatorMsg];
          setState((prev) => ({
            ...prev,
            currentRound: startData.round,
            isIterating: true,
            awaitingFeedback: null,
            discussion: {
              ...prev.discussion,
              messages: messagesRef.current,
            },
          }));
          break;
        }

        case 'iteration.research': {
          const researchData = data as {
            round: number;
            queries: string[];
            newSources: number;
            informationGain: number;
          };
          // Update or create the round entry for research data
          const existingResearchIdx = iterationsRef.current.findIndex(
            (r) => r.round === researchData.round
          );
          if (existingResearchIdx >= 0) {
            iterationsRef.current = iterationsRef.current.map((r, idx) =>
              idx === existingResearchIdx
                ? {
                    ...r,
                    research: {
                      queries: researchData.queries,
                      newSources: researchData.newSources,
                      informationGain: researchData.informationGain,
                    },
                  }
                : r
            );
          } else {
            iterationsRef.current = [
              ...iterationsRef.current,
              {
                round: researchData.round,
                score: 0,
                previousScore: 0,
                gaps: { dataGaps: [], ideaGaps: [] },
                research: {
                  queries: researchData.queries,
                  newSources: researchData.newSources,
                  informationGain: researchData.informationGain,
                },
                timestamp: new Date(),
              },
            ];
          }
          setState((prev) => ({
            ...prev,
            iterations: iterationsRef.current,
          }));
          break;
        }

        case 'iteration.ideas': {
          const ideasData = data as {
            round: number;
            newInsights: Array<{ title: string }>;
            newCreativeIdeas: Array<{ title: string }>;
            totalInsights: number;
            totalCreativeIdeas: number;
          };
          const existingIdeasIdx = iterationsRef.current.findIndex(
            (r) => r.round === ideasData.round
          );
          if (existingIdeasIdx >= 0) {
            iterationsRef.current = iterationsRef.current.map((r, idx) =>
              idx === existingIdeasIdx
                ? {
                    ...r,
                    ideas: {
                      newInsights: ideasData.newInsights,
                      newCreativeIdeas: ideasData.newCreativeIdeas,
                      totalInsights: ideasData.totalInsights,
                      totalCreativeIdeas: ideasData.totalCreativeIdeas,
                    },
                  }
                : r
            );
          } else {
            iterationsRef.current = [
              ...iterationsRef.current,
              {
                round: ideasData.round,
                score: 0,
                previousScore: 0,
                gaps: { dataGaps: [], ideaGaps: [] },
                ideas: {
                  newInsights: ideasData.newInsights,
                  newCreativeIdeas: ideasData.newCreativeIdeas,
                  totalInsights: ideasData.totalInsights,
                  totalCreativeIdeas: ideasData.totalCreativeIdeas,
                },
                timestamp: new Date(),
              },
            ];
          }
          setState((prev) => ({
            ...prev,
            iterations: iterationsRef.current,
          }));
          break;
        }

        case 'iteration.demo': {
          const demoData = data as {
            round: number;
            status: 'generating' | 'completed';
          };
          const existingDemoIdx = iterationsRef.current.findIndex(
            (r) => r.round === demoData.round
          );
          if (existingDemoIdx >= 0) {
            iterationsRef.current = iterationsRef.current.map((r, idx) =>
              idx === existingDemoIdx
                ? { ...r, demo: { status: demoData.status } }
                : r
            );
          } else {
            iterationsRef.current = [
              ...iterationsRef.current,
              {
                round: demoData.round,
                score: 0,
                previousScore: 0,
                gaps: { dataGaps: [], ideaGaps: [] },
                demo: { status: demoData.status },
                timestamp: new Date(),
              },
            ];
          }
          setState((prev) => ({
            ...prev,
            iterations: iterationsRef.current,
          }));
          break;
        }

        case 'iteration.eval': {
          const evalData = data as {
            round: number;
            score: number;
            previousScore: number;
            gaps: { dataGaps: string[]; ideaGaps: string[] };
            record?: string;
          };
          const existingEvalIdx = iterationsRef.current.findIndex(
            (r) => r.round === evalData.round
          );
          let updatedRound: IterationRound;
          if (existingEvalIdx >= 0) {
            iterationsRef.current = iterationsRef.current.map((r, idx) => {
              if (idx === existingEvalIdx) {
                updatedRound = {
                  ...r,
                  score: evalData.score,
                  previousScore: evalData.previousScore,
                  gaps: evalData.gaps,
                  record: evalData.record,
                };
                return updatedRound;
              }
              return r;
            });
          } else {
            updatedRound = {
              round: evalData.round,
              score: evalData.score,
              previousScore: evalData.previousScore,
              gaps: evalData.gaps,
              record: evalData.record,
              timestamp: new Date(),
            };
            iterationsRef.current = [...iterationsRef.current, updatedRound];
          }
          const evalMsg: DiscussionMessage = {
            id: `iter_eval_${evalData.round}_${Date.now()}`,
            agentRole: 'director' as DiscussionRole,
            agentName: '评估系统',
            agentIcon: 'bar-chart-2',
            content: `── 第 ${evalData.round} 轮评估完成 ──\n得分: ${evalData.score.toFixed(0)}/100${evalData.previousScore > 0 ? ` (${evalData.score > evalData.previousScore ? '+' : ''}${(evalData.score - evalData.previousScore).toFixed(1)})` : ''}\n数据差距: ${evalData.gaps.dataGaps.slice(0, 3).join('、') || '无'}\n创意差距: ${evalData.gaps.ideaGaps.slice(0, 3).join('、') || '无'}`,
            phase: 'synthesis' as DiscussionPhase,
            messageType: 'system' as DiscussionMessageType,
            timestamp: new Date(),
          };
          messagesRef.current = [...messagesRef.current, evalMsg];
          setState((prev) => ({
            ...prev,
            iterations: iterationsRef.current,
            discussion: {
              ...prev.discussion,
              messages: messagesRef.current,
            },
          }));
          callbacksRef.current?.onIterationUpdate?.(
            iterationsRef.current.find((r) => r.round === evalData.round)!
          );
          break;
        }

        case 'iteration.awaiting_feedback': {
          const fbData = data as {
            round: number;
            score: number;
            gaps: { dataGaps: string[]; ideaGaps: string[] };
            timeoutMs: number;
          };
          setState((prev) => ({
            ...prev,
            awaitingFeedback: {
              round: fbData.round,
              score: fbData.score,
              gaps: fbData.gaps,
              timeoutMs: fbData.timeoutMs,
            },
          }));
          break;
        }

        case 'iteration.exit': {
          const exitData = data as {
            reason: string;
            finalScore: number;
            totalIterations: number;
            sessionId?: string;
          };
          // Capture report/sessionId synchronously via setState updater,
          // then fire callbacks outside to avoid side-effects in updater.
          let capturedReport: DeepResearchReport | null = null;
          let capturedSessionId: string | null = null;
          setState((prev) => {
            capturedReport = prev.discussion.report;
            // Prefer the authoritative originalSessionId from the exit event
            capturedSessionId = exitData.sessionId ?? prev.discussion.sessionId;
            return {
              ...prev,
              discussion: {
                ...prev.discussion,
                phase: 'completed' as DiscussionPhase,
                sessionId: capturedSessionId,
                typingAgent: null,
              },
              exitReason: exitData.reason,
              finalScore: exitData.finalScore,
              totalIterations: exitData.totalIterations,
              isIterating: false,
              awaitingFeedback: null,
            };
          });
          callbacksRef.current?.onIterationExit?.(exitData);
          if (capturedReport && capturedSessionId) {
            callbacksRef.current?.onComplete?.({
              report: capturedReport,
              sessionId: capturedSessionId,
            });
          }
          if (abortControllerRef.current) {
            abortControllerRef.current.abort();
            abortControllerRef.current = null;
          }
          break;
        }

        case 'error': {
          const errorData = data as { message: string };
          setState((prev) => ({
            ...prev,
            discussion: {
              ...prev.discussion,
              phase: 'error',
              error: errorData.message,
              typingAgent: null,
            },
            isIterating: false,
          }));
          callbacksRef.current?.onError?.(errorData.message);
          if (abortControllerRef.current) {
            abortControllerRef.current.abort();
            abortControllerRef.current = null;
          }
          break;
        }
      }
    },
    []
  );

  const startResearch = useCallback(
    async (query: string, researchOptions?: IterativeResearchOptions) => {
      cleanup();

      messagesRef.current = [];
      directionsRef.current = [];
      iterationsRef.current = [];
      modeRef.current = researchOptions?.mode ?? 'single';

      const activeMode = researchOptions?.mode ?? 'single';

      setState({
        ...initialIterativeState,
        mode: activeMode,
        discussion: {
          ...initialDiscussionState,
          phase: 'ideation',
        },
      });

      try {
        const url = `/ai-studio/projects/${projectId}/deep-research/stream`;

        const body: Record<string, unknown> = {
          query,
          mode: activeMode,
          options: researchOptions
            ? {
                maxRounds: researchOptions.maxRounds,
                includeAcademic: researchOptions.includeAcademic,
                language: researchOptions.language,
                depth: researchOptions.depth,
              }
            : undefined,
        };

        if (activeMode === 'iterative') {
          body.iterationOptions = {
            maxIterations: researchOptions?.maxIterations ?? 4,
            qualityThreshold: researchOptions?.qualityThreshold ?? 0.75,
            autoGenerateDemo: researchOptions?.autoGenerateDemo ?? true,
          };
        }

        const abortController = new AbortController();
        abortControllerRef.current = abortController;

        const response = await fetch(config.streamApiUrl + url, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            Accept: 'text/event-stream',
            ...getAuthHeader(),
          },
          body: JSON.stringify(body),
          signal: abortController.signal,
        });

        if (!response.ok) {
          throw new Error(`HTTP error! status: ${response.status}`);
        }

        const reader = response.body?.getReader();
        if (!reader) {
          throw new Error('No response body');
        }

        const decoder = new TextDecoder();
        let buffer = '';
        let currentEvent = '';
        let currentData = '';

        while (true) {
          const { done, value } = await reader.read();
          if (done) break;

          buffer += decoder.decode(value, { stream: true });

          const lines = buffer.split('\n');
          buffer = lines.pop() || '';

          for (const line of lines) {
            if (line.startsWith('event:')) {
              currentEvent = line.slice(6).trim();
            } else if (line.startsWith('data:')) {
              currentData = line.slice(5).trim();
            } else if (line === '' && currentEvent && currentData) {
              try {
                const parsed = JSON.parse(currentData) as Record<
                  string,
                  unknown
                >;
                handleEvent(currentEvent, parsed);
              } catch (e) {
                logger.error('Failed to parse SSE data:', e);
              }
              currentEvent = '';
              currentData = '';
            }
          }
        }

        setState((currentState) => {
          if (
            currentState.discussion.phase !== 'completed' &&
            currentState.discussion.phase !== 'idle' &&
            currentState.discussion.phase !== 'error'
          ) {
            logger.warn(
              `SSE stream ended in phase "${currentState.discussion.phase}" without completion. Backend may still be running.`
            );
            callbacksRef.current?.onStreamEndIncomplete?.();
            return {
              ...currentState,
              discussion: {
                ...currentState.discussion,
                phase: 'error',
                error: '研究流连接中断，后端可能仍在处理中',
              },
              isIterating: false,
            };
          }
          return currentState;
        });
      } catch (error: unknown) {
        if (error instanceof Error && error.name === 'AbortError') {
          return;
        }
        const errorMessage =
          error instanceof Error ? error.message : '研究启动失败';
        setState((prev) => ({
          ...prev,
          discussion: {
            ...prev.discussion,
            phase: 'error',
            error: errorMessage,
          },
          isIterating: false,
        }));
        callbacksRef.current?.onError?.(errorMessage);
      }
    },
    [projectId, cleanup, handleEvent]
  );

  const sendFeedback = useCallback(
    async (text: string) => {
      try {
        // Add user feedback as a visible message in the chat
        const userMsg: DiscussionMessage = {
          id: `user_feedback_${Date.now()}`,
          agentRole: 'user' as DiscussionRole,
          agentName: '用户反馈',
          agentIcon: 'user',
          content: text,
          phase: 'ideation' as DiscussionPhase,
          messageType: 'user' as DiscussionMessageType,
          timestamp: new Date(),
        };
        messagesRef.current = [...messagesRef.current, userMsg];
        setState((prev) => ({
          ...prev,
          discussion: {
            ...prev.discussion,
            messages: messagesRef.current,
          },
        }));

        await fetch(
          `${config.apiBaseUrl}/api/v1/ai-studio/projects/${projectId}/deep-research/feedback`,
          {
            method: 'POST',
            headers: { 'Content-Type': 'application/json', ...getAuthHeader() },
            body: JSON.stringify({ feedback: text }),
          }
        );
        setState((prev) => ({ ...prev, awaitingFeedback: null }));
      } catch (err) {
        logger.error('Failed to send feedback:', err);
      }
    },
    [projectId]
  );

  const stop = useCallback(() => {
    cleanup();
    setState((prev) => {
      // Trigger recovery polling if research was actually running
      if (
        prev.discussion.phase !== 'idle' &&
        prev.discussion.phase !== 'completed'
      ) {
        callbacksRef.current?.onStreamEndIncomplete?.();
      }
      return {
        ...prev,
        discussion: {
          ...prev.discussion,
          phase: prev.discussion.phase === 'idle' ? 'idle' : 'error',
          error: prev.discussion.phase !== 'idle' ? '研究已取消' : null,
          typingAgent: null,
        },
        isIterating: false,
      };
    });
  }, [cleanup]);

  const reset = useCallback(() => {
    cleanup();
    messagesRef.current = [];
    directionsRef.current = [];
    iterationsRef.current = [];
    setState(initialIterativeState);
  }, [cleanup]);

  // eslint-disable-next-line react-hooks/exhaustive-deps -- cleanup only on unmount
  useEffect(() => {
    return () => {
      if (abortControllerRef.current) {
        abortControllerRef.current.abort();
        abortControllerRef.current = null;
      }
    };
  }, []);

  const discussionPhase = state.discussion.phase;

  return {
    state,
    startResearch,
    stop,
    reset,
    isActive:
      discussionPhase !== 'idle' &&
      discussionPhase !== 'completed' &&
      discussionPhase !== 'error',
    sendFeedback,
  };
}

export default useIterativeResearch;
